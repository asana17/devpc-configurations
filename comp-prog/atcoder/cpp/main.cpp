#include <bits/stdc++.h>

using namespace std;
int N;

using ll = long long;

template <class T>
void debug_vector(vector<T> v)
{
  int index = 0;
  for (auto c : v) {
    cout << "index " << index << ": " << c << endl;
    index++;
  }
}

int main(int argc, char *argv[])
{
  cin >> N;

  return 0;
}
